def main():
    setup_default_logging()
    args = parser.parse_args()

    # import model_list
    from tools.model_list import model_list
    data_split = ['val', 'test']
    # evaluate all models on the validation set and test set
    for cur_split in data_split:
        args.split = cur_split
        pred_list_path = 'output/IP102_' + cur_split + '_pred_list.pth'
        labels_path = 'output/IP102_' + cur_split + '_labels.pth'
        if os.path.exists(pred_list_path):
            pred_list = torch.load(pred_list_path)
            labels = torch.load(labels_path)
        else:
            # 计算每个模型的输出
            pred_list = torch.tensor([])
            labels = None
            for cur_model in model_list:
                args.model = cur_model['model_name']
                args.checkpoint = cur_model['checkpoint']
                args.img_size = cur_model['input_size']
                if labels is None:
                    pred_list, labels = validate(args, return_labels=True)
                else:
                    pred_list = torch.cat((pred_list, validate(args)))
            labels = labels.cpu()
            # 保存备用
            torch.save(pred_list, pred_list_path)
            torch.save(labels, labels_path)


if __name__ == '__main__':
    main()
